score=int(input("enter the score:"))
if score>=90:
    print("the grade is 'A'")
elif score>=80:
    print("the grade is 'B'")
elif score>=70:
    print("the grade is 'C'") 
elif score>=60:
    print("the grade is 'D'")
else:
    print("the grade is 'F'")               